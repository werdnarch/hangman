export default function useRandomWord() {
  const [word, setWord] = useState<string>();

  const { category } = useGameContext();

  function getRandomWord(
    category:
      | "Movies"
      | "TV Shows"
      | "Countries"
      | "Capital Cities"
      | "Animals"
      | "Sports"
  ) {
    let randomArray: string[] = [];

    switch (category) {
      case "Sports":
        randomArray = sports; // no 'let' here
        break;
      case "Animals":
        randomArray = animals;
        break;
      case "Countries":
        randomArray = countries;
        break;
      case "Capital Cities":
        randomArray = capitals;
        break;
      case "Movies":
        randomArray = movies;
        break;
      case "TV Shows":
        randomArray = tvShows;
        break;
      // add other cases
    }

    const randomIndex = Math.floor(Math.random() * randomArray.length);

    return { randomIndex };
  }

  useEffect(() => {
    if (!category) return;

    let randomArray: string[] = [];

    switch (category) {
      case "Sports":
        randomArray = sports; // no 'let' here
        break;
      case "Animals":
        randomArray = animals;
        break;
      case "Countries":
        randomArray = countries;
        break;
      case "Capital Cities":
        randomArray = capitals;
        break;
      case "Movies":
        randomArray = movies;
        break;
      case "TV Shows":
        randomArray = tvShows;
        break;
      // add other cases
    }

    const randomIndex = Math.floor(Math.random() * randomArray.length);
    setWord(randomArray[randomIndex]);
  }, [category]);

  return { word, setWord, getRandomWord };
}
